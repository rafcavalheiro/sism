  <p align="justify" class="card-text">O sistema desenvolvido apresenta características de um Smart Grid (Sistema Elétrico Inteligente)
  voltado para monitoramento doméstico. Este software foi desenvolvido para integração com um Smart Meter Electabuzz, protótipo criado com Arduino.
  O usuário é livre para escolher qual eletrodoméstico deseja monitorar, desde que este esteja cadastrado no sistema e corretamente acoplado ao Smart Meter.<br> 
  O sistema irá registrar em tempo real o consumo em kWh do aparelho usado, e irá calcular a tarifa em R$ automaticamente.
  Todos os monitoramentos ficarão resgistrados no sistema para consulta.</p><br> 
  
